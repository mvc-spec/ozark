package org.mvcspec.ozark.binding.convert;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marker annotation to enable the MVC specific binding rules.
 *
 * @author Christian Kaltepoth
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.PARAMETER, ElementType.METHOD, ElementType.FIELD})
@Documented
public @interface MvcBinding {

    /**
     * Whether or not use the request locale for locale dependent parsing operations
     */
    boolean useRequestLocale() default true;

}
